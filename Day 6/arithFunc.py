def arithmeticOperations(a , b):
    print(f"Sum : {a + b}")
    print(f"Difference : {a - b}")
    print(f"Product : {a * b}")
    print(f"Division : {a / b}")
    print(f"Floor Division : {a // b}")
    print(f"Modulus : {a % b}")
    print(f"Exponentiation : {a ** b}")
    
arithmeticOperations(20, 2.5)
