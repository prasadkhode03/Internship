import mathops

num1 = int(input("Enter first number : "))
num2 = int(input("Enter second number : "))

print("Addition : ", mathops.add(num1, num2))
print("Subtraction : ", mathops.subtract(num1, num2))
print("Mutliplication : ", mathops.multiply(num1, num2))
print("Division : ", mathops.divide(num1, num2))